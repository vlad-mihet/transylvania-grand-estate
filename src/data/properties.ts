export function getPropertyBySlug(slug: string): Property | undefined {
  return properties.find((p) => p.slug === slug);
}

export function getFeaturedProperties(): Property[] {
  return properties.filter((p) => p.featured);
}

export function getPropertiesByCity(citySlug: string): Property[] {
  return properties.filter((p) => p.location.citySlug === citySlug);
}

export function getSimilarProperties(
  property: Property,
  limit = 3
): Property[] {
  return properties
    .filter(
      (p) =>
        p.id !== property.id &&
        (p.location.citySlug === property.location.citySlug ||
          p.type === property.type)
    )
    .slice(0, limit);
}

export function getPropertiesByDeveloper(developerId: string): Property[] {
  return properties.filter((p) => p.developerId === developerId);
}
